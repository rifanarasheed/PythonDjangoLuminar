# Arithematic Operator(+,-,*,/,%,//(floor division),**)

num1 = int(input("enter the number1"))
num2 = int(input("enter the number2"))
addition = num1 + num2
print("added :",addition)
subtraction = num1 - num2
print("subtracted :",subtraction)
multiplication = num1 * num2
print("multiplied :",multiplication)
modulo = num1%num2               # gives remainder
print("modulo",modulo)
floor_division = num1//num2      # floor division discard the point value
print("floor division :",floor_division)